# Calculation of distance between co-ordinates

def calculate_factorial_float(number):
    assert number >= 0, "There is no negative factorial"
    if number == 0:
        factorial = 1
    else:
        factorial = 1
        for i in range(1, number + 1):
            factorial *= i
    return float(factorial)


